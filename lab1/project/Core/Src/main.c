typedef enum {
    S1,
    S2
} LEDState_t;

LEDState_t current_state = S1;
int s1_step = 0;
TaskHandle_t xLEDTaskHandle = NULL;
xQueueHandle xStateQueue;

void LED_Task(void *pvParameters) {
    uint8_t dummy;
    for (;;) {
        if (current_state == S1) {
            uint16_t pin;
            if      (s1_step == 0) pin = GPIO_PIN_14;
            else if (s1_step == 1) pin = GPIO_PIN_13;
            else                   pin = GPIO_PIN_12;

            HAL_GPIO_WritePin(GPIOD, pin, GPIO_PIN_SET);

            if (xQueueReceive(xStateQueue, &dummy, pdMS_TO_TICKS(1000)) == pdPASS) {
                current_state = S2;
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14, GPIO_PIN_RESET);
                continue;
            }

            HAL_GPIO_WritePin(GPIOD, pin, GPIO_PIN_RESET);
            s1_step = (s1_step + 1) % 3;
        } else {
            HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);

            if (xQueueReceive(xStateQueue, &dummy, pdMS_TO_TICKS(500)) == pdPASS) {
                current_state = S1;
                s1_step = (s1_step + 1) % 3;
                HAL_GPIO_WritePin(GPIOD, GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_14, GPIO_PIN_RESET);
            }
        }
    }
}

void Button_Task(void *pvParameters) {
    uint32_t start_time;
    uint8_t sig = 1;
    bool is_suspended = false;

    for (;;) {
        if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET) {
            vTaskDelay(20); // 消抖
            if (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET) {
                start_time = xTaskGetTickCount();

                while (HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_0) == GPIO_PIN_SET) {
                    vTaskDelay(20);
                }

                uint32_t duration = xTaskGetTickCount() - start_time;

                if (duration >= 1000) {
                    if (!is_suspended) {
                        vTaskSuspend(xLEDTaskHandle);
                        is_suspended = true;
                    } else {
                        vTaskResume(xLEDTaskHandle);
                        is_suspended = false;
                    }
                } else {
                    if (!is_suspended) {
                        xQueueSend(xStateQueue, &sig, 0);
                    }
                }
            }
        }
        vTaskDelay(50);
    }
}

